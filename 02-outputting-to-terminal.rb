# Print is a simple way to output to terminal.
print "This is a string! " # Print doesn't force a new-line, so it should be used carefully.

# Puts adds a new-line, so it is usually easier to read output.
puts "This one ends with a new-line!"

# Inspect shows us the raw value, good for differentiating between strings vs. numbers, etc.
puts "Hello, World!".inspect

# Short form of "puts value.inspect" is "p"
p "Hello, World!"

p 3