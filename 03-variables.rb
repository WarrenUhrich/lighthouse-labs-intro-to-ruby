# Convention is snake_case for variable naming
my_name = "Jaqueline"
my_name = "John" # Variables can be re-assigned

puts my_name

# Starting a variable with a capital letter implies it should be
# treated as a CONSTANT.
MY_NAME = "Manpreet"
MY_NAME = "Raymond" # You'll get a warning if you try to re-assign a constant

puts my_name, MY_NAME
